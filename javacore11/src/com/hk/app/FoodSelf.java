package com.hk.app;

public class FoodSelf {
	
	private String name;
	private int price;
	
	// 생성자를 통한 초기화 추가 가능
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	

}
